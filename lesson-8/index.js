function Student(name, salary, rate) {
    this.name = name;
    this.salary = salary;
    this.rate = rate;

    this.getCreditSummary = function() {
        const availabelRating = rateCollection[this.rate];

        return availabelRating ? this.salary * availabelRating : 0;
    }
}

